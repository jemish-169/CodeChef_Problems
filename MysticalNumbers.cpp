/*
Mystical Numbers
*/
#include <bits/stdc++.h>
using namespace std;
int main()
{
    cout << (1 & 1) << " ";
    cout << (1 & 0) << " ";
    cout << (0 & 0) << " ";
    cout << (0 & 1) << " ";
    cout << (1 ^ 1) << " ";
    cout << (1 ^ 0) << " ";
    cout << (0 ^ 0) << " ";
    cout << (0 ^ 1) << " ";

    return 0;
}